		<h2>House Arrest</h2>


		<p>
			Aung San Suu Kyi was placed under house arrest for a total of 15 years over a <strong> 21-year period </strong>, on numerous occasions, since she began her political career, during which time she was prevented from meeting her party supporters and international visitors. In an interview, she said that while under house arrest she spent her time reading philosophy, politics and biographies that her husband had sent her. She also passed the time playing the piano, and was occasionally allowed visits from foreign diplomats as well as from her personal physician.
		</p>

		<p>
			Although under house arrest, Aung San Suu Kyi was granted permission to leave Burma under the condition that she never return, which she refused: "As a mother, the greater sacrifice was giving up my sons, but I was always aware of the fact that others had given up more than me. I never forget that my colleagues who are in prison suffer not only physically, but mentally for their families who have no security outside- in the larger prison of Burma under authoritarian rule." Her loyalty to the people of Burma and her solidarity with those imprisoned for their pro-democratic acts have earned her deep respect among the Burmese people.
		</p>

		<p>
			The media were also prevented from visiting Aung San Suu Kyi, as occurred in 1998 when journalist Maurizio Giuliano, after photographing her, was stopped by customs officials who then confiscated all his films, tapes and some notes. In contrast, Aung San Suu Kyi did have visits from government representatives, such as during her autumn 1994 house arrest when she met the leader of Burma, General Than Shwe and General Khin Nyunt on 20 September in the first meeting since she had been placed in detention. On several occasions during her house arrest, she had periods of poor health and as a result was hospitalized.
		</p>

		<p>
			The Burmese government detained and kept Aung San Suu Kyi imprisoned because it viewed her as someone "likely to undermine the community peace and stability" of the country, and used both Article 10(a) and 10(b) of the 1975 State Protection Act (granting the government the power to imprison people for up to five years without a trial), and Section 22 of the "Law to Safeguard the State Against the Dangers of Those Desiring to Cause Subversive Acts" as legal tools against her. She continuously appealed her detention, and many nations and figures continued to call for her release and that of 2,100 other political prisoners in the country. On 12 November 2010, days after the junta-backed Union Solidarity and Development Party (USDP) won elections conducted after a gap of 20 years, the junta finally agreed to sign orders allowing Suu Kyi's release, and Suu Kyi's house arrest term came to an end on 13 November 2010.
		</p>

